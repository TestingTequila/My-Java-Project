package com.janbask.java.day11.ForLoop;

public class ForLoop3
{
    public static void main(String[] args) {
        System.out.println("======Print Number from 10-1=================");

        for (int x =10;x>0;x--)
        {
            System.out.println(x);//10, 9, .....2, 1

        }
    }
}

