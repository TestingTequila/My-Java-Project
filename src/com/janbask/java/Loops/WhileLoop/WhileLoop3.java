package com.janbask.java.Loops.WhileLoop;

public class WhileLoop3
{
    public static void main(String[] args) {
        System.out.println("======Print Number from 10-1=================");
        int x =10;
        while (x>0)
        {
            System.out.println(x);//10, 9, .....2, 1
            x--;
        }
    }
}

